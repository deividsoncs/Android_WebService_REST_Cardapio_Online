    private void atualizarComboEstabelecimento() {
       if (STATUS!=0){
            //Carrega o ComboBox Estabelecimentos como os Estabelecimento do Banco
            listaEstabelecimento.setLista(new EstabelecimentoReq().getListaEstabelecimentoPorLogin(tfLogin.getText()).getLista());	
            jcbEstabelecimento.removeAllItems();
            listaIdentificadorEstabelecimento.removeAll(listaIdentificadorEstabelecimento);
            if (!listaEstabelecimento.isEmpty()){
                for (int registro = 0; registro < listaEstabelecimento.getLista().size(); registro++){ 
                        //System.out.println(listaEstabelecimento.getLista().get(registro).getNomeFantasia()); 
                        jcbEstabelecimento.addItem(listaEstabelecimento.getLista().get(registro).getNomeFantasia());
                        listaIdentificadorEstabelecimento.add((int) listaEstabelecimento.getLista().get(registro).getId());
                }
            }else{
                JOptionPane.showMessageDialog(null, "Nenhum estabelecimento encontrado, por favor cadastre um estabelecimento!");
            }
        }
    }

    private void atualizarJListTipoEstabelecimento() {
        listaIdentificadorJlistTipo.removeAll(listaIdentificadorTipo);
        DefaultListModel modelListaTipo = new DefaultListModel();
        //Recupera no Banco a List dos TipoEstabelecimento do ID do estabelecimento selecionado no jcbEstabelecimentos
        listaJlistTipoRelacionados.setLista(new TipoEstabelecimentoReq().getListaTipoEstabelecimentoPorIdEstabelecimento(listaIdentificadorEstabelecimento.get(jcbEstabelecimento.getSelectedIndex())).getLista());
        if (listaJlistTipoRelacionados.getLista() != null){
            for (int registro = 0; registro < listaJlistTipoRelacionados.getLista().size(); registro++){
                modelListaTipo.addElement(listaJlistTipoRelacionados.getLista().get(registro).getTipoEstabelecimento());
                listaIdentificadorJlistTipo.add((int) listaJlistTipoRelacionados.getLista().get(registro).getId());
            }
            jListTipoEstabelecimentos.setModel(modelListaTipo);
        }else{
            //Acertar isso!
            JOptionPane.showMessageDialog(null, "Não existe nehum Tipo cadastrado para o estabelecimento selecionado!");
        }
    }

