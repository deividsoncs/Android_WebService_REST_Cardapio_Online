        //Preenche os campos JTextField do cadastro do endereço se este estiver cadastro no Banco!
        Endereco endereco = new EnderecoReq().getEnderecoPorIdEstabelecimento(Long.valueOf(listaIdentificadorEstabelecimento.get(jcbEstabelecimento.getSelectedIndex())));
