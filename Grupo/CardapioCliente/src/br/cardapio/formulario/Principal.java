    private void btLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogarActionPerformed
        // TODO add your handling code here:
        btLogar.setEnabled(false);
        btLogOff.setEnabled(true);

        UsuarioEstabelecimento usuarioEstabelecimentoLogon = new UsuarioEstabelecimento();
        UsuarioEstabelecimento usuarioEstabelecimentoBanco;

        String senha = new String(pfSenha.getPassword());
        usuarioEstabelecimentoLogon.setLogin(tfLogin.getText());
        usuarioEstabelecimentoLogon.setSenha(senha);
        usuarioEstabelecimentoBanco = new UsuarioEstabelecimentoReq().autentica(usuarioEstabelecimentoLogon);

        if ((usuarioEstabelecimentoBanco  != null)&&(usuarioEstabelecimentoBanco.getId()!=0)){
            //Controle de navegação
            tfLogin.setEnabled(false);
            pfSenha.setEnabled(false);
            jtpPrincipal.setVisible(true);
            
            JOptionPane.showMessageDialog(null, "Logado como: " + usuarioEstabelecimentoBanco.getLogin());
            System.out.println(usuarioEstabelecimentoLogon.getId());
           
            ID_USUARIO = Long.valueOf(usuarioEstabelecimentoBanco.getId());
            STATUS = 1;

            //Carrega ComboBox Estabelecimento:
            atualizarComboEstabelecimento();
            
            //Carrega o Tipo do Estabelecimento
            atualizarJcbTipoEstabelecimento();

            //Carrega ComboBox dos Serviços
            atualizaJcbServicos();
            
            //Torna invisivel a JPane Secao Cardapio
            jPSecaoCardapio.setVisible(false);

        }else{
            JOptionPane.showMessageDialog(null, "Senha ou Login incorreto ou inexistente!");
            STATUS = 0;
            ID_USUARIO = 0;
            tfLogin.setText("");
            pfSenha.setText("");
            btLogar.setEnabled(true);
            btLogOff.setEnabled(false);
            jtpPrincipal.setVisible(false);
        }

    }//GEN-LAST:event_btLogarActionPerformed

