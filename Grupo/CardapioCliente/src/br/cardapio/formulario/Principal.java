    private void btSalvarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarItemActionPerformed
        // TODO add your handling code here:
        if (jcbSecaoCardapio.getItemCount()>0){
            if(!tfIdItemCardapio.getText().equals("") || tfIdItemCardapio.getText().equals("0")){
                ItemCardapio item = new ItemCardapio();
                item.setIdSecaoCardapio(listaIdentificadorSecaoCardapio.get(jcbSecaoCardapio.getSelectedIndex()));
                item.setNomeItem(tfNomeItem.getText());
                item.setPreco(Double.valueOf(tfPrecoItem.getText()));
                item.setPercentualDesconto(Double.valueOf(tfPercentualDesconto.getText()));
                item.setDescricao(tfDescricao.getText());
                //Coloca a data no padrão brasileiro.
                Date data = new Date();
                SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
                formatador.format(data);
                item.setDataModificacao(data);
                //Foto!
                JOptionPane.showMessageDialog(null, new ItemCardapioReq().adiciona(item));
                limpaItem();
                atualizarJlistItems();
            }else{
                JOptionPane.showMessageDialog(null, "Não a Item selecionado para alteraçao!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cadastre uma seção cardapio primeiro!");
        }
        
    }//GEN-LAST:event_btSalvarItemActionPerformed

